    private void pesquisar() {
        usuarioDao objDAO = new usuarioDao();
        ResultSet objRst = objDAO.selecionarTaxas();
        double taxaa, taxam;

        try {
            while (objRst.next()) {
                taxaa = objRst.getDouble("juro_anual");
                taxam = objRst.getDouble("juro_mensal");
                lbtxaa.setText(taxaa + "%");
                lbtxma.setText(taxam + "%");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,
                    "Erro de execução no Select:" + ex.getMessage());
        }
    }

