    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        usuarioDao objusu = new usuarioDao();
        Usuario usu = new Usuario();
        if (txtntxm.getText().length() == 0 && txtntxa.getText().length() == 0) {
            lberromodtx.setText("Preencha pelo menos 1 campo!");

        } else if (txtntxm.getText().length() > 0 && txtntxa.getText().length() == 0) {
            usu.setTaxam(Double.parseDouble(txtntxm.getText()));
            objusu.mudtaxaM(usu);
            lberromodtx.setText("Taxa mensal foi alterada com sucesso!");
        } else if (txtntxm.getText().length() == 0 && txtntxa.getText().length() > 0) {
            usu.setTaxaa(Double.parseDouble(txtntxa.getText()));
            objusu.mudtaxaA(usu);
            lberromodtx.setText("Taxa anual foi alterada com sucesso!");
        } else if (txtntxm.getText().length() > 0 && txtntxa.getText().length() > 0) {
            usu.setTaxaa(Double.parseDouble(txtntxa.getText()));
            usu.setTaxam(Double.parseDouble(txtntxm.getText()));
            objusu.mudtaxaA(usu);
            objusu.mudtaxaM(usu);
            lberromodtx.setText("As taxas foram alteradas com sucesso!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

